package misc;

public class RectangleBound {
	double x;
	double y;
	double xLength;
	double yLength;
	
	public RectangleBound(double x, double y, double xLength, double yLength) {
		this.x = x;
		this.y = y;
		this.xLength = xLength;
		this.yLength = yLength;
	}
	
	
	
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	public double getXLength() {
		return xLength;
	}
	public double getYLength() {
		return yLength;
	}

}
